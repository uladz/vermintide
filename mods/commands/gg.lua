local draw = function(frame)
	for x = 1, #animation[frame] do
		local text = ""

		for y = 1, #animation[frame][x] do
			if animation[frame][x][y] == 0 then
				text = text .. "	"
			else
				text = text .. "Z"
			end
		end

		Mods.chat.send(text)
	end
end

local animation_repeat = 0
local animation_start = 0
local animation_frame = 1

if #args == 0 then
	Mods.chat.animation = function(self, dt, t)
		if t - animation_start > 0.1 then

			draw(animation_frame)

			animation_start = t;
			animation_frame = animation_frame + 1

			if #animation < animation_frame then
				animation_repeat = animation_repeat + 1
				animation_frame = 1
			end

			if animation_repeat >= 3 then
				Mods.chat.animation = nil
			end
		end
	end

	return true
else
	return false
end
